<?php

declare(strict_types=1);

namespace App\Telegram\Handler;

use SergiX44\Nutgram\Nutgram;

class HelpHandler
{
    public function __invoke(Nutgram $bot): void
    {
        $bot->sendMessage(
            text: <<<'MSG'
            📋 Команды:

            /list — текущие дела
              /list все — со снузом и выполненными
              /list done — что уже сделано
              /list snoozed — что отложено

            /done — пометить выполненным
            /snooze — отложить
            /free <время> [где] — что взять, если есть свободное окно
              Примеры: /free 2h, /free 30m дома, /free 1h на улице

            /deps, /block, /unblock — связи между задачами
            /reset — забыть наш текущий разговор

            💬 Свободный текст

            Можно просто писать как другу — я пойму:
            «Купить молоко в магазине», «Что у меня сегодня?»,
            «Сделал тренировку», «Отложи дочери костюм на субботу».

            Я помню последние ~10 сообщений (30 минут) — поэтому
            «отложи её на завтра» сработает, найду нужную сам.
            Reply на моё сообщение — тоже подхвачу контекст.

            Когда ты просишь сделать что-то массовое (создать 5 задач,
            отменить несколько) — я переспрошу подтверждение кнопками.
            Просто чтобы случайно не натворить лишнего.
            MSG,
        );
    }
}
